public class Variabile {
    public static void main(String[] args) {
        //declarare si initializare variabile
        String marcaTelefon = "Samsung";
        String modelTelefon = "A52";
        //java este strongly typed > trebuie sa specificam tipul de date
        System.out.println("Am cumparat un telefon marca : " + marcaTelefon);
        System.out.println("Este modelul : " + modelTelefon);
        //suprascriere
        modelTelefon = "S22";
        //declarare variabila
        String nume;
        String prenume;
        //initializare variabila
        prenume = "Mada";
        nume = "Anderson";
        //concatenare de stringuri :
        System.out.println(prenume +" "+ nume );

    }

}
